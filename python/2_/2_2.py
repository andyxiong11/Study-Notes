# 变量命名
# 变量名只能包含字母、数字和下划线。以字母或下划线打头，不能以数字打头。
# 变量名只可使用下划线来分隔其中的单词。
# 不要将Python关键字和函数名用作变量名。
# 变量名应既简短又具有描述性。
# 慎用小写字母l和大写字母O，因为它们可能被人错看成数字1和0。
# 注意，应尽量使用小写的Python变量名。在变量名中使用大写字母虽然不会导致错误，但避免使用大写字母是个不错的主意。
